			<p class="textoN">
				La Universidad de San Martín de Porres - USMP tiene como visión ser una institución líder socialmente responsable y por ello viene asumiendo el compromiso de trabajo en la lucha por la mitigación y adaptación al cambio climático, así como el desarrollo de propuestas sostenibles de responsabilidad social y ambiental.

			</p>

			<p class="textoN">
				Nuestra Universidad, desde el año 2011 es parte de la Red Ambiental Interuniversitaria (RAI) la cual agrupa a una comunidad de universidades, articuladas por el  MINAM, a través de la Dirección General de Educación, Cultura y Ciudadanía Ambiental. 

			</p>

			<p class="textoN">
				Por otra parte, nuestra casa de estudio ha obtenido, por dos años consecutivos (2012 y 2013), el Distintivo Empresa Socialmente Responsable que otorga Perú 2021; este distintivo es un reconocimiento al esfuerzo por la incorporación de la Responsabilidad Social a nivel de todas las partes interesadas, relacionadas con la USMP.

			</p>
			<p class="textoN">
				Por lo antes mencionado, es que desde Julio del presente año se conformó el Comité de Responsabilidad Social, el cual busca integrar todas las acciones en materia ambiental y responsabilidad social con la finalidad de establecer estrategias que permitan incorporar ambas gestiones en la USMP. 
			</p>
			<p class="textoN">
				El Comité impulsará proyectos sociales con la participación de docentes y alumnos, desarrollará estrategias para la gestión ecoeficiente de nuestras operaciones y será un órgano de consulta en el ámbito de sus funciones.
			</p>
			<p class="textoN">
			Nuestro principal objetivo es <strong>“Articular los esfuerzos de todas las facultades e institutos de la USMP con la finalidad de generar una estrategia de gestión de responsabilidad social.”</strong>
